for i in range(10): # 0 to 9
    print(i)

for i in range(1, 11): # 1 to 10
    print(i)

for i in range(10, 0, -1): # 10 to 1 decreasing
    print(i)
for i in reversed(range(1, 11)): # 10 to 1 decreasing
    print(i)

for i in range(2, 20, 2): # 2, 4, 6, ..., 18
    print(i)

for i in range(0, 19, 3): # 0, 3, 6, ..., 18
    print(i)